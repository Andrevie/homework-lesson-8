mood = "relaxed"

if mood == "happy":
    print("It is great to see you happy! ")

elif mood == "nervous":
    print("Take a deep breath 3 times.")

elif mood == "sad":
    print("Dry your tears!")

elif mood == "relaxed":
    print("Good to be relaxed!")

elif mood == "excited":
    print("Jump up and down!")

else:
    print("I don't recognize this mood!")
